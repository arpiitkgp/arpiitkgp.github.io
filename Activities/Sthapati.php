<h5><b>Sthapati</b></h5><hr>

<p>Sthapati is the annual magazine published by the students of the department, incorporating their hard work and creativity under able guidance and help from the esteemed faculty members. It is
a compilation of articles, interviews,
research papers, compositions and
updates from the field of architecture
all over the globe. Eminent professors
contribute their expertise in
architecture research field and the
alumni pass their practical experiences
from their initial days of learning and
work, thus making it an ideal digest
of architectural information of refined
taste.<br>
Looking at the continuously
improving standards of our
magazine our department was also
entrusted with the responsibility
of publishing Indian Arch 2014 for
NASA. With the experience gained by
publishing Indian Arch at such a gift scale, we
have remarkably raised the standards of
Sthapati 2015 from being just an annual
magazine. And now we are all set to further
ameliorate Sthapati 2016. Sthapati portrays the
true spirit of our department by showcasing our
achievements, airing our thoughts and raising
pertinent issues in the field of architecture. It
has been a legacy of all the generations of
successful architects and planners, IIT
Kharagpur has endowed the society with.<br>


</p>

<font size="4"><b><u>REACH OF THE MAGAZINE</u></b></font>

<p>In an effort to reach out to every
KGPian, the copies of the magazine
are sent to the Central Library of
the Institute, every Hall of Residence,
all members of the faculty,
all HOD's and departmental libraries
and also circulated among a vast
proportion of the student community
and dealers in the neighbouring
commercial centers. All our alumni
all over the world receive a copy of
Sthapati.<br>

The magazine has interviews and articles from
eminent and renowned personalities like Dr
A.P.J. Abdul Kalam, Ar. Hafeez Contractor and
renowned Indian architects like Ar. Bijoy Jain,
Ar. Brinda Somaya, Ar. Bijoy Ramachandran
and globally renowned architects like Fariborz
Sahba, Aziza Chaouni, Italian architect Carlo
Ratti, teaching at MIT, USA, and internationally
renowned researcher, author and educator
Vivian Loftness from Carnegie Mellon
University and a Danish architect and urban
design consultant Jan Gehl and innovative
associations like Madstock. This guarantees
a wide readership for the magazine. The
magazine has received appreciation and praise
from all its readers.

</p>

