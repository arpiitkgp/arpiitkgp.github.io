<p>The Department of Architecture and Regional Planning at the Indian Institute of Technology, Kharagpur has been a pioneer in architectural education in the country since its inception in 1952, and today, it continues to play a significant role in the emerging concerns and planned development of the built environment of the country.

Our flagship Bachelor of Architecture (Honours) degree is a full-fledged 5-year undergraduate programme that is offered by only two IITs. This academic excellence is augmented by enthusiastic student activities, most prominently the ones associated with the Students' Society of Architects and Planners (SSAP).

Our students have excelled not only within the department but in other departments as well. Unlike other departments, exposure to the corporate environment is imbibed in their curriculum through a 6-month office training in the 8th semester of their course, thus bridging the gap between theory and practice. Hence, they are competent in all aspects and wiII definitely be able to face the challenges ahead with aplomb. I urge you to avaiI of our talent pool and we look forward to forging a fruitful, long-term relationship with your company by making our students a part of your esteemed organization.
